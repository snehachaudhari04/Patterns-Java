
class Pattern5
{
    void star(int n) {
        for(int j=n;j>=1;j--){
                for(int k=1;k<=j;k++){
                System.out.print(k+" ");
            }
            System.out.println();
            
        }
        
        
    }
 public static void main(String args[]){ 
    Pattern5 p= new Pattern5();
    p.star(5);
   
 }
} 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 